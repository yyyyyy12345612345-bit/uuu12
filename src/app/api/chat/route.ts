export async function POST(req: NextRequest) {
  try {
    const { messages, userData } = await req.json();

    if (!messages || !Array.isArray(messages)) {
      return NextResponse.json({ error: "Invalid request" }, { status: 400 });
    }

    const systemPrompt = getSystemPrompt(userData);

    // Call Val Town API with the correct payload structure expected by the Val Town endpoint
    const response = await fetch(VAL_TOWN_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        messages,
        systemContext: systemPrompt
      })
    });

    if (!response.ok) {
      const errText = await response.text();
      console.error("Val Town API failed with status:", response.status, errText);
      return NextResponse.json({ 
        reply: `عذراً، حدث خطأ في الاتصال بـ Val Town (الحالة: ${response.status}).\nالسبب: ${errText || "لا توجد تفاصيل"}` 
      });
    }

    const data = await response.json();
    
    // Return standard reply field mapping 'text' from Val Town to 'reply' for ChatBot component
    const replyText = data.text || data.reply || "عذراً، لم أتمكن من الحصول على رد.";

    return NextResponse.json({
      reply: replyText
    });

  } catch (error: any) {
    console.error("[Chat API Error]:", error);
    return NextResponse.json({ reply: `حدث خطأ في الاتصال بالسيرفر المحلي. التفاصيل: ${error.message || error}` });
  }
}
