  // 3. فحص طلبات تغيير الدولة (Change Country Interception)
  const changeCountryMatch = textClean.match(/^(?:غير|عدل|تغيير|تعديل|تحديث)\s+(?:بلد|دوله|الدوله|الدولة|البلد|محافظه|محافظتي)\s+(?:الى|لـ|ل|يكون)?\s*(.+)/i);
