/**
 * Fetches portrait-oriented nature photos from Pexels (paginated),
 * dedupes by id, and writes src/data/pexelsPortraitNature.generated.ts
 *
 * Usage: PEXELS_API_KEY=your_key node scripts/generate-portrait-nature.mjs
 * (PowerShell: $env:PEXELS_API_KEY="..."; node scripts/generate-portrait-nature.mjs)
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function loadKey() {
  const k = process.env.PEXELS_API_KEY?.trim();
  return k ?? "";
}

const QUERIES = [
  "nature",
  "forest",
  "ocean",
  "mountain",
  "sunset landscape",
  "river",
  "waterfall",
  "desert",
  "sky clouds",
  "beach",
  "flowers field",
  "lake",
  "jungle",
  "snow mountain",
  "meadow",
  "tropical",
  "autumn forest",
  "northern lights",
  "canyon",
  "valley",
];

const TARGET_UNIQUE = 2950;
const PER_PAGE = 80;

async function fetchPage(apiKey, query, page) {
  const u = new URL("https://api.pexels.com/v1/search");
  u.searchParams.set("query", query);
  u.searchParams.set("orientation", "portrait");
  u.searchParams.set("per_page", String(PER_PAGE));
  u.searchParams.set("page", String(page));
  const res = await fetch(u, { headers: { Authorization: apiKey } });
  if (!res.ok) throw new Error(`${res.status} ${await res.text()}`);
  return res.json();
}

function extractExistingIds() {
  const bgPath = path.join(root, "src", "data", "backgrounds.ts");
  const text = fs.readFileSync(bgPath, "utf8");
  const ids = new Set();
  for (const m of text.matchAll(/pexels\.com\/photos\/(\d+)\//g)) {
    ids.add(Number(m[1]));
  }
  return ids;
}

function tagFromQuery(q) {
  const base = ["طبيعة", "طولي", "nature"];
  if (q.includes("forest") || q.includes("jungle")) base.push("غابات", "forest");
  if (q.includes("ocean") || q.includes("beach")) base.push("بحار", "sea");
  if (q.includes("mountain") || q.includes("snow")) base.push("جبال", "mountain");
  if (q.includes("sunset")) base.push("غروب", "sunset");
  if (q.includes("sky")) base.push("سماء", "sky");
  if (q.includes("desert")) base.push("صحراء");
  if (q.includes("river") || q.includes("lake") || q.includes("waterfall")) base.push("ماء");
  return base;
}

async function main() {
  const apiKey = loadKey();
  if (!apiKey) {
    console.error("Set PEXELS_API_KEY in the environment (same key as in .env.local for Next).");
    process.exit(1);
  }

  const skipIds = extractExistingIds();
  const seen = new Set(skipIds);
  const items = [];

  outer: for (const query of QUERIES) {
    let page = 1;
    while (page <= 80) {
      const data = await fetchPage(apiKey, query, page);
      const photos = data.photos ?? [];
      if (photos.length === 0) break;

      for (const photo of photos) {
        const id = photo.id;
        if (seen.has(id)) continue;
        seen.add(id);
        const src = photo.src?.portrait || photo.src?.large2x || photo.src?.large;
        if (!src) continue;
        const tags = tagFromQuery(query);
        items.push({ id, src, tags });
        if (items.length >= TARGET_UNIQUE) break outer;
      }

      if (!data.next_page) break;
      page += 1;
      await new Promise((r) => setTimeout(r, 350));
    }
    console.error(`query "${query}" done, total ${items.length}`);
    if (items.length >= TARGET_UNIQUE) break;
  }

  const outPath = path.join(root, "src", "data", "pexelsPortraitNature.generated.ts");
  const lines = [
    "/** Auto-generated by scripts/generate-portrait-nature.mjs — do not edit by hand */",
    "",
    "export const PEXELS_PORTRAIT_NATURE_BACKGROUNDS = [",
  ];
  for (const { src, tags } of items) {
    const tagStr = tags.map((t) => JSON.stringify(t)).join(", ");
    lines.push(`  { type: "image" as const, src: ${JSON.stringify(src)}, poster: "", tags: [${tagStr}] },`);
  }
  lines.push("];", "");
  fs.writeFileSync(outPath, lines.join("\n"), "utf8");
  console.log(`Wrote ${items.length} items to ${path.relative(root, outPath)}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
